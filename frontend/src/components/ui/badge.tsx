import * as React from "react";
import { cva, type VariantProps } from "class-variance-authority";

import { cn } from "@/lib/utils";

const badgeVariants = cva(
  "inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-medium transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default: "border-transparent bg-primary text-primary-foreground hover:bg-primary/80",
        secondary: "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        destructive: "border-transparent bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline: "text-foreground",
        // Status variants for workflow
        draft: "border-transparent bg-muted text-muted-foreground",
        submitted: "border-transparent bg-info/10 text-info",
        approved: "border-transparent bg-success/10 text-success",
        rejected: "border-transparent bg-destructive/10 text-destructive",
        pending: "border-transparent bg-warning/10 text-warning",
        // Role badges
        admin: "border-transparent bg-primary/10 text-primary",
        manager: "border-transparent bg-info/10 text-info",
        contributor: "border-transparent bg-success/10 text-success",
        viewer: "border-transparent bg-muted text-muted-foreground",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  },
);

export interface BadgeProps extends React.HTMLAttributes<HTMLDivElement>, VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return <div className={cn(badgeVariants({ variant }), className)} {...props} />;
}

export { Badge, badgeVariants };
